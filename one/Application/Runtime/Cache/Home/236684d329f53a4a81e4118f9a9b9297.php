        		    1. 什么是薪计划？
                    <p>薪计划是人人贷WE理财针对工薪族推出的高效、便捷的自动投标工具，每月固定日期加入固定资金，轻松攒钱同时享受高收益，快速积累财富实现每个心愿。</p>
                    </li>
                    <li>2. 薪计划的发布时间？
                        <p>每月1-25号上午10:30。</p>
                    </li>
                    <li >3. 薪计划的收益有多少？
                        <p>预期年化收益率8%，但根据系统自动投标的标的利率不同和借款人的实际履约情况不同，每个出借人的实际收益率存在小许差异。</p>
                    </li>
                    <li>4. 出借利息收益处理方式？
                        <p>薪计划的出借利息所获收益处理方式是收益再投资，所得利息会自动循环复投，充分实现复利效应</p>
                    </li>
                    <li>5. 薪计划的期限有多久？
                        <p>薪计划的锁定期为12个月，在锁定期内薪计划的回款本息由系统自动实现循环复投，充分提高资金利用效率，锁定期结束后，自动通过债权转让退出。<br />薪计划不支持提前退出。</p>
                    </li>
                   <li>6. 到了每月支付日如何支付？
                       <p>a.系统自动划扣：您可以在每月支付日前将相应金额充值到民生银行存管账户，在月支付日当天24:00系统会从您的民生银行存管账户中自动划扣完成支付。<br />
                           b. 手动支付：每月支付日的前3天，您也可以在充值后手动进行支付；进入【我的账户】-【账户总览】-【薪计划】，选择您要支付的一期薪计划，点击【支付本期出借金额】，跳转到【支付】详情页后，点击【确定】按钮即可支付完成。
                           <br> c.手动补充支付：若您在某月未能按时支付当月出借金额，可在下月支付日前进行补充支付；进入【我的账户】-【账户总览】-【薪计划】，选择您要补充支付的薪计划，点击【补充支付上一期】，跳转【补充加入】详情页后，点击【确定】按钮即可补充支付完成。
                           <br>您支付的每月出借金额，均将进入您民生银行存管账户，并由系统按照约定为您进行自动出借。
